<template>
  <v-card tile flat>
    <v-card-title class="py-2">
      <span>Total size: {{ totalSize | readableSize }}</span>
    </v-card-title>
    <v-card-actions>
      <div class="pa-2">
        <v-chip
          v-for="(p, index) of paths"
          :key="index"
          @click="(e) => onChipClick(e, index)"
        >
          {{ p }}{{ sep }}
        </v-chip>
      </div>
    </v-card-actions>
  </v-card>
</template>

<script>
import path from 'path'
import { mapState, mapGetters } from 'vuex'

export default {
  data() {
    return {
      sep: path.sep
    }
  },
  computed: {
    ...mapState('local', ['node']),
    ...mapGetters('local', ['totalSize', 'paths'])
  },
  methods: {
    onChipClick(e, index) {
      this.$emit('click:chip', index)
    }
  }
}
</script>

<style scoped lang="scss">
.v-card > .v-card__actions {
  overflow: auto;
  padding: 0;
  > div {
    white-space: nowrap;
  }
}
</style>
